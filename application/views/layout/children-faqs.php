            <div class="col-xs-12 col-sm-6">
              <h3>
                Can I Use Mediation To Settle Disputes About The Children?
              </h3>
              <p>
                Yes. Most child issues are settled through negotiation and
                mediation. Court is often seen as a last resort. The mediation
                process is less formal than the court process and many of our
                clients report that they find mediation less stressful than the
                idea of going to court. Agreeing arrangements also gives you
                more of a sense of control over the issues. And it’s possible to
                formalise anything you agree with your ex spouse through
                mediation by making the agreement part of a Consent Order.  Once
                you get this approved by a family judge it’s legally binding.
              </p>
            </div>

            <div class="col-xs-12 col-sm-6">
              <h3>
                Do we have to go to Court to decide what will happen to the
                children?
              </h3>
              <p>
                No, in almost all cases this is not required. Typically, the
                parents agree and often formalise the arrangement with the
                assistance of their lawyer. Where parents agree there is usually
                no need for any court order at all. If the parents cannot agree
                it would then become a matter for the Court to decide.
              </p>
            </div>

            <div class="col-xs-12 col-sm-6">
              <h3>
                Can I insist that I see my child on special occasions, such as
                birthdays?
              </h3>
              <p>
                Normally the non-resident parent will be able to agree to see
                their child on significant dates and occasions. This can either
                be agreed informally with the other parent but if there is a
                dispute then the non-resident parent could apply for a court
                order.
              </p>
            </div>
          </div>
        </div>

