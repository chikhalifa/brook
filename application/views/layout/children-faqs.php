          <div class="row row-flex row-padded wide-gutter">
            <div class="col-xs-12 col-sm-6">
              <h3>Can I take my child to live abroad after divorce?</h3>
              <p>
                Permission will be required from the other parent or via a court
                order. Taking your child abroad for longer than a holiday would
                be considered abduction and the other parent could apply to the
                Child Abduction Unit to have your child brought home.
              </p>
            </div>

            <div class="col-xs-12 col-sm-6">
              <h3>
                What happens to step children regarding custody and maintenance?
              </h3>
              <p>
                If the step-parent has taken the role of parent during the
                marriage and their ex-spouse (the biological parent) is not able
                to provide for the child, the step-parent may then be required
                to pay child maintenance.  Whilst it is typical for the child’s
                biological parent to gain residence, this is not always the
                case. It will depend on how capable the parents are to provide
                for the child what is in the child’s best interests.
              </p>
            </div>

            <div class="col-xs-12 col-sm-6">
              <h3>
                Does the CMS (formerly the CSA) have to be involved in deciding
                child maintenance?
              </h3>
              <p>
                No. Whilst you may use a CMS calculation to aid with deciding a
                maintenance payment figure, it is then common for a private
                maintenance agreement to be set up by your lawyers. The CMS or
                court do not then need to be involved. If required, 12 months
                from the agreement, the CMS can become involved if either party
                wishes it.
              </p>
            </div>

            <div class="col-xs-12 col-sm-6">
              <h3>Can I change my child’s surname after divorce?</h3>
              <p>
                Changing your child’s surname would require the other parent’s
                written consent or a court order.
              </p>
            </div>
          </div>
        </div>

        <div>
          <div class="row row-flex row-padded wide-gutter">
            <div class="col-xs-12 col-sm-6">
              <h3>
                Will the family home be retained for the child to live in?
              </h3>
              <p>
                If it is in the best interests of the child to remain in the
                family home with one of the parents it is possible that the
                court will order the home to be retained for a set length of
                time.
              </p>
            </div>

            <div class="col-xs-12 col-sm-6">
              <h3>Can I move with my child to another part of the UK?</h3>
              <p>
                If you are the resident parent, you do not need the other
                parent’s permission to move with your child within the UK.
                However, the other parent could request a court order preventing
                you from moving whilst the situation is looked at in more
                detail. In making a decision as to the child’s living
                arrangements, the court will always look first at what is in the
                best interests of the child.
              </p>
            </div>

            <div class="col-xs-12 col-sm-6">
              <h3>Can I take my child abroad on holiday?</h3>
              <p>
                If you have a court order setting out that the child is to live
                with you then you do not need ask permission from the other
                parent to go on holiday abroad for up to 28 days. If you do not
                have a court order, you will require the permission of the other
                parent. If the other parent will not give permission, you can
                apply for an order to go on holiday with the child.
              </p>
            </div>

            <div class="col-xs-12 col-sm-6">
              <h3>
                Will my ex-spouse having a new partner affect what contact they
                can have with our child?
              </h3>
              <p>
                Typically no. However, if you have good reason to be concerned
                about your child’s welfare with regard to your ex’s new partner,
                you could apply for conditions to be imposed upon the existing
                contact arrangements.
              </p>
            </div>
          </div>
