              <h3>Can our child choose which parent they live with?</h3>
              <p>
                If the child is old enough and has sufficient understanding to
                state a preference, the court will take into account their
                wishes. It is however unlikely that the child will simply be
                asked to choose because it is an unfair question and the reasons
                behind a child’s answer may be mixed. The court will however
                also consider wider factors, including each parent’s ability to
                meet the child’s needs, the child’s safety and how the new
                change in circumstances may affect the child.
