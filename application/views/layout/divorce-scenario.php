				<div class="inner reply">
					<h4>Our Solution</h4>

					<p>
						The critical question is for the court to establish that Bernard's
						share of the particular properties are his in his own right, so any
						orders that the court might make between Arthur and his wife will
						respect Bernard's interests. This is achieved by making an
						application to intervene in the matrimonial proceedings so that
						Bernard's interests can be protected. That enables us to represent
						Bernard rather than him being dependent on his brother simply
						asserting it- with the risk of being disbelieved.
					</p>
				</div>
