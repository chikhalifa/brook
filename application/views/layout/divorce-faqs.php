                <h3>Can My Spouse Object To No-Fault Divorce?</h3>
                <p>
                As there is no longer an element of blame in the divorce
                application the request for a divorce can’t be contested.
                However if negotiations around maintenance, child issues and
                financial settlements are inconclusive the court will have
                to intervene and decide matters. This will involve a
                contested court hearing. If matters aren’t resolved
                informally between you and your spouse or civil partner, you
                may wish to consider arbitration or other forms of family
                mediation.<span data-ccp-props="{}"> </span>
                </p>
            </div>

            <div class="col-xs-12 col-sm-6">
                <h3>Will No-Fault Divorce Be Cheaper?</h3>
                <p>
                Not necessarily. Even though you or your spouse won’t have
                to legally blame the other for the breakdown in your
                marriage you still have to agree a financial settlement, how
                your children are to be financially supported and what their
                living arrangements will be. That said, it’s logical to
                assume that if the acrimony is removed from the process,
                couples may find it easier to reach agreements in these
                matters. This in turn will reduce the time and money spent
                on legal fees.
                <span data-ccp-props="{}"> </span>
                </p>
            </div>

            <div class="col-xs-12 col-sm-6">
                <h3>Will A No-Fault Divorce Be Quicker?</h3>
                <p>
                No-fault divorce doesn’t mean a ‘quickie’ divorce as some
                have suggested. You can’t apply for a conditional order
                until 20 weeks have elapsed from the date you issued your
                application. There is then a further six-week period before
                you can turn your conditional divorce order into a final
                order. And, as always you should settle any differences
                between you and your estranged spouse in relation to
                finances and children before a no-fault divorce is
                finalised.
                </p>
