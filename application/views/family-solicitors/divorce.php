                    <h2>What is the procedure for no-fault divorce?</h2>
                    <p>
                      <span data-contrast="auto"
                        >No fault divorce removes the need for a spouse to
                        select one of the five reasons for the breakdown of
                        their marriage (such as ‘unreasonable behaviour’ on the
                        part of their spouse, or adultery). Instead:</span
                      ><span
                        data-ccp-props='{"134233117":true,"134233118":true,"201341983":2,"335559739":120,"335559740":360}'
                        > </span
                      >
                    </p>
                    <ul>
                      <li>
                        <span data-contrast="auto">
                          Either or both parties to a marriage or civil
                          partnership may apply to the court for a divorce
                          order. The order dissolves the marriage on the sole
                          ground that it has broken down irretrievably.</span
                        >
                      </li>
                      <li>
                        <span data-contrast="none">
                          The application for a divorce order must be
                          accompanied by a statement by the applicant or
                          applicants that the marriage has broken down
                          irretrievably.</span
                        >
                      </li>
                      <li>
                        <span data-contrast="none">
                          Parties can make a joint application to the
                          court</span
                        ><span data-contrast="none">.</span>
                      </li>
                      <li>
                        <span data-contrast="none">
                          The court must firstly take the statement to be
                          conclusive evidence that the marriage has broken down
                          irretrievably; and secondly it must make a divorce
                          order.</span
                        >
                      </li>
                      <li>
                        <span data-contrast="none">
                          Neither party can oppose the application</span
                        ><span data-contrast="none">.</span>
                      </li>
                      <li>
                        <span data-contrast="none">
                          The divorce order is initially a ‘conditional order’
                          (previously this was known as a decree nisi)</span
                        ><span data-contrast="none">.</span>
                      </li>
                      <li>
                        <span data-contrast="none">
                          A conditional order can only be made if the applicant
                          or applicants (previously the petitioner(s)) confirm
                          they want the divorce to go ahead</span
                        ><span data-contrast="none">.</span>
                      </li>
                      <li>
                        <span data-contrast="none">
                          This confirmation cannot be made until a</span
                        ><span data-contrast="none">
                          minimum of 20 weeks has passed from the date on which
                          proceedings began. This is to enable the applicant or
                          applicants a meaningful period of reflection on the
                          decision to divorce.</span
                        ><span data-ccp-props="{}"> </span>
                      </li>
                      <li>
                        <span data-contrast="none">
                          During this 20-week period couples should discuss
                          child arrangements and financial settlements and
                          attempt to reach an agreement</span
                        ><span data-contrast="none">.</span>
                      </li>
                      <li>
                        After a minimum period of six weeks the conditional
                        order may be converted into a final order (similar to
                        the decree absolute).
                      </li>
                    </ul>
                    <h2>No-fault divorce in practice</h2>
                    <p>
                      <span data-contrast="auto"
                        >In practice the no-fault divorce procedure is geared
                        toward more efficient, less acrimonious divorce cases.
                        In particular the removal of the ability of either side
                        to contest proceedings will reduce the likelihood of
                        expensive, contested court applications cropping up
                        throughout the process. It’s also hoped there will be
                        less of an impact on children and the wider family as
                        divorce becomes a less contentious issue. When the
                        opportunity for bitter disputes over personal matters is
                        reduced, couples can concentrate more productively on
                        reaching a fair financial settlement. </span
                      ><span data-ccp-props="{}"> </span>
                    </p>
