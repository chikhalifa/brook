                    <h2>No-fault divorce in the UK</h2>
                    <p>
                      <span data-contrast="auto"
                        >From its inception divorce has been a procedure
                        associated with blame and fault. In England and Wales
                        this is borne out by The Matrimonial Causes Act, 1973
                        the statute that regulates divorce. Under the
                        legislation separating couples must establish one of
                        five grounds to divorce – three of which involve blaming
                        the other spouse. As society has evolved, criticism has
                        grown about this need to prove fault to end a marriage –
                        not least because it raises the possibility of
                        additional conflict in what may be an already fractious
                        relationship. Following many years of political lobbying
                        and pressure the first divorces under the no-fault
                        procedure are set to begin in England and Wales on 6
                        April 2022. </span
                      ><span data-ccp-props="{}"> </span>
                    </p>
