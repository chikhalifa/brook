                    <p>
                        When it comes to
                        <a href="<?= base_url('family-solicitors')?>">divorce</a>, judges in the
                        family courts have a wide discretion to
                        redistribute income and assets. Their main goal is
                        to produce a result that’s fair to both sides.
                        There’s no set formula – each case is different
                        and will be judged on its own individual facts –
                        but certain factors must be taken into account.
                        While we cannot predict exactly what would be
                        awarded we can certainly advise you, within
                        certain limits, on the kind of financial
                        settlement you can expect.
                    </p>
                    <p>
                        We always attempt to agree a settlement, rather
                        than see a case go to a full, contested court
                        hearing where a judge will have to impose a
                        settlement on the parties. An agreed settlement is
                        often quicker and will usually resolve a dispute
                        more amicably. Also, the legal costs involved in a
                        negotiated settlement will probably be less than
                        if the matter proceeded to a contested court
                        hearing. If a satisfactory settlement is not
                        possible though, we would advise issuing an
                        application that ultimately could go before a
                        Judge to be decided.
                    </p>
                    <p>
                        Below we’ve set out the types of orders a court
                        has the power to make in relation to financial
                        settlements and the factors a judge must consider
                        when making such orders. This page is meant as a
                        guide only, so please feel free to ask us any
                        questions.
                    </p>
                    <h2>1. Orders The Court Can Make</h2>
                    <p>The orders available are as follows.</p>
                    <ul>
                        <li>
                        <strong>Maintenance pending suit: </strong
                        >Maintenance for a spouse/civil partner to tide
                        him/her over while the divorce is being
                        finalised. It stops once the divorce/dissolution
                        of a civil partnership is concluded.
                        </li>
                        <li>
                        <strong>Periodical Payments: </strong
                        >Maintenance for an ex-spouse/civil partner
                        which will start after divorce/dissolution. It
                        can be open-ended or limited in time.
                        </li>
                        <li>
                        <strong>Secured Periodical Payments: </strong
                        >Payments which are secured against property or
                        assets to ensure payment. This is a rare type of
                        order, but will be considered in exceptional
                        circumstances.
                        </li>
                        <li>
                        <strong>Lump Sum Order: </strong>Payment of a
                        lump sum of money from one spouse/civil partner
                        to the other. There is no limit on the amount
                        that can be awarded and it can be paid by
                        instalments.
                        </li>
                        <li>
                        <strong>Property Orders: </strong>Various orders
                        redistributing interests in property (usually
                        the family home) can be made. These include a
                        transfer of property from one spouse/civil
                        partner to the other and a sale of the property
                        which, if ordered, would include a direction on
                        how the proceeds are to be divided. We deal more
                        fully wit this aspect of financial settlements
                        in Section 3 below.
                        </li>
                        <li>
                        <strong>Pension Orders: </strong>The court can
                        order pension sharing or earmark part of the
                        spouse/civil partner’s Pension Scheme for the
                        benefit of the other spouse/civil partner.
                        </li>
                        <li>
                        <strong>Variation of Settlement Order: </strong
                        >This gives the courts power to vary settlements
                        or trusts that have been set up in favour of one
                        spouse/civil partner. This is a rare type of
                        order.
                        </li>
                    </ul>
                    <h2>
                        2. Financial Orders: Factors The Court Must
                        Consider
                    </h2>
                    <p>
                        When deciding what financial orders to make the
                        court has a duty to consider all the circumstances
                        of the case. Primary consideration must always be
                        given to the welfare of any minor child of the
                        family. The court will want to ensure that any
                        minors have adequate accommodation. The specific
                        factors  the court must consider are as follows:
                    </p>
                    <ul>
                        <li>
                        The income, earning capacity, property and other
                        financial resources of each party, including
                        what each party is likely to have in the
                        foreseeable future e.g. by way of inheritance.
                        </li>
                        <li>
                        The financial needs, obligations and
                        responsibilities of each party.
                        </li>
                        <li>
                        The standard of living the family enjoyed before
                        the marriage broke down.
                        </li>
                        <li>
                        The age of each party and the length of the
                        marriage/civil partnership.
                        </li>
                        <li>Any disability of either party.</li>
                        <li>
                        Contributions made by each party to the welfare
                        of the family, including contributions to be
                        made in the foreseeable future.
                        </li>
                        <li>
                        The conduct of each party but only if that
                        conduct is extreme.
                        </li>
                        <li>
                        The value to each party of any benefit that they
                        will lose as a result of the divorce e.g.
                        pension rights.
                        </li>
                    </ul>
                    <p>
                        In medium to long marriages the starting point is
                        equality, but it is important to note it is only a
                        starting point. Different factors can push
                        decisions on finances either way e.g.
                        inheritances, exceptional contributions, gross bad
                        behaviour etc. As we’ve said each case must be
                        assessed on its own facts.
                    </p>
                    <p>
                        The Court will also specifically consider making
                        each party financially independent of the other;
                        to achieve what’s known as a “clean break”. While
                        a clean break sounds attractive in theory, in
                        practice it is often not as good as it sounds – so
                        carefully consult us before agreeing to this type
                        of settlement. .
                    </p>
                    <h2>3. Division Of The Family Home</h2>
                    <p>
                        The matrimonial/civil partnership property is
                        often a couple’s most valuable asset. However, it
                        usually also provides a home for the family and
                        therefore must be given special consideration. A
                        useful starting point is often to consider whether
                        it is reasonable and financially possible for one
                        of the parties to remain in the home (particularly
                        the party with whom any children are to reside
                        following divorce). If this is possible then the
                        property can be transferred to that party, perhaps
                        with an adjustment in the other party’s favour in
                        terms of the amount of any lump sum that may
                        become payable under the financial settlement. If
                        enabling one party to remain in the family home is
                        not financially possible, then the property could
                        be transferred on the basis that it will be sold
                        at a later date (usually when the remaining
                        spouse/civil partner remarries or forms a further
                        civil partnership or the children complete
                        full-time education) when the vacating
                        spouse/civil partner can receive a share of the
                        proceeds. (This is known as a “Martin Order”).
                    </p>
                    <p>
                        Consideration would also have to be given to
                        whether the remaining spouse/civil partner can
                        discharge any mortgage on the family home and
                        whether the mortgage company will agree to the
                        mortgage being transferred into that party’s sole
                        name. If a transfer from one party to the other is
                        not appropriate, the court may decide that the
                        property must remain in the joint names with a
                        sale taking place at a later date. (This is known
                        as a “Mesher Order”). Consideration may also be
                        given to a sale of the property and division of
                        the proceeds – especially where the size or value
                        of the house was excessive to each party’s needs.
                    </p>
                    <h2>
                        4. When Orders Can Be Made And Their Duration
                    </h2>
                    <p>
                        An application can be made for a financial order
                        any time after the filing of the
                        divorce/dissolution or (judicial) separation
                        application. The court can make an order for
                        maintenance pending suit any time after the issue
                        of the divorce application but can only make
                        financial orders after the conditional order of
                        divorce or dissolution. Property orders are not
                        effective until the issuing of the final order of
                        divorce or (judicial) separation order.
                    </p>
                    <p>
                        Any order for periodical payments should state how
                        long such payments are to last or alternatively
                        leave the duration open-ended. Either party can
                        re-apply to the Court at any time before the order
                        ends to vary the maintenance payments (upwards or
                        downwards). This would normally be appropriate if
                        there had been a change in circumstances. Because
                        these types of order depend on joint lives, you
                        might need to consider life insurance in case the
                        payer dies during the term of the order.
                    </p>
                    <p>
                        Capital orders (i.e. lump sums and property
                        orders) are normally phrased as being final. This
                        means that neither party can re-apply to the Court
                        at any stage in the future to vary the capital
                        division.
                    </p>
                    <p>
                        It is important to note that remarriage or forming
                        a subsequent civil partnership bars you from
                        making a claim against your former spouse/civil
                        partner if you have not initiated such a claim
                        before remarriage. Further, your remarriage or
                        subsequent civil partnership ends any entitlement
                        to maintenance that was previously ordered to be
                        paid by your former spouse/civil partner.
                    </p>
                    <h2>5. The Procedure</h2>
                    <p>
                        If agreement can be reached, then the solicitors
                        on behalf of the parties can draw up an agreement
                        known as a Consent Application. This is signed by
                        the solicitors and lodged with the Court, where it
                        is made into a formal Court order, known as a
                        Consent Order. If agreement cannot be reached,
                        then one or other party will have to make an
                        application to the Court. We list below the steps
                        taken before such an application is heard finally
                        by a Judge.
                    </p>
                    <h3>a. Application</h3>
                    <p>
                        Your solicitor will prepare an application which
                        will initiate the process. On submission to the
                        Court, a hearing date will be fixed, known as the
                        First Directions Appointment  or ‘First
                        Appointment’ (FDA) some 12 to 16 weeks ahead. A
                        strict timetable is then applied within which the
                        parties must each prepare a financial statement
                        known as Form E which the parties are required to
                        fill out and exchange. The purpose of the form is
                        to enable both sides to provide the Court with
                        full details of their finances. If the form is
                        complete and there are no further material
                        questions left unanswered, then the chance of
                        using the First Appointment as a Financial Dispute
                        Resolution Appointment is considerably improved.
                        In advance of the FDA the parties must each
                        prepare a concise statement of issues, a
                        chronology and a questionnaire setting out any
                        further questions they want to ask or any further
                        documentation required of the other party.
                    </p>
                    <p>
                        A notice must also be lodged with the court
                        stating whether the party is able to proceed with
                        the next stage (Financial Dispute Resolution
                        Appointment or FDR) at the FDA thereby short
                        circuiting matters. As we’ve said this is usually
                        only possible when the disclosure given is
                        relatively complete at an early stage and the
                        issues are not too complex. For each hearing in
                        the process the parties must also prepare an
                        estimate of their legal costs to that point.
                    </p>
                    <h3>b. The First Directions Appointment (FDA)</h3>
                    <p>
                        The Court will give directions as to what
                        questions are to be answered, whether any
                        valuations are to be obtained etc. We usually
                        recommend instructing a barrister for this
                        appointment and probably we will have a
                        preliminary meeting beforehand. A further
                        timetable will be fixed and a Judge may adjourn
                        the FDA or direct that the Financial Dispute
                        Resolution Appointment (FDR) must take place. This
                        will usually be fixed a few months ahead. In the
                        interim both parties must comply with all the
                        directions made at the FDA. We can leapfrog this
                        FDA by agreement but experience tells us that –
                        except in the simplest cases – that is not
                        desirable.
                    </p>
                    <h3>
                        c. The Financial Dispute Resolution Appointment
                        (FDR)
                    </h3>
                    <p>
                        This hearing is designed to define the issues and
                        encourage the parties to reach agreement. Before
                        the appointment each party must have submitted
                        their proposals to the court. Note that:
                    </p>
                    <ul>
                        <li>
                        No order can be made unless the matter is
                        agreed.
                        </li>
                        <li>
                        If agreement is reached then the Court can make
                        an order thus finalising matters.
                        </li>
                        <li>
                        If no agreement is reached then further
                        directions can be given and the matter fixed for
                        a Final Hearing.
                        </li>
                        <li>
                        The Judge who conducts the appointment is
                        disbarred from adjudicating at the final
                        hearing.
                        </li>
                        <li>
                        If the FDR does not settle the matter the court
                        will list it for a final hearing usually some
                        months ahead.
                        </li>
                    </ul>
                    <p>
                        Where your solicitor attempts to negotiate a
                        settlement on your behalf, the correspondence may
                        be marked “Without Prejudice”. Any communication
                        labelled this way will not be revealed to the
                        court if the matter proceeds to a final hearing,
                        enabling the party who made a without prejudice
                        offer to suggest an entirely different order to
                        the court. The reason to make a without prejudice
                        offer is to enable the parties to negotiate freely
                        without being held to any offers if the matter is
                        not settled. If a without prejudice offer is
                        accepted however it then becomes open and cannot
                        remain private to the parties.
                    </p>
                    <h3>d. The Final Hearing</h3>
                    <p>
                        If it is necessary for there to be a final
                        hearing, then both parties must attend court to
                        give oral evidence and be cross-examined by the
                        other party’s legal representative. This hearing
                        usually takes place in front of a District Judge
                        and no-one, other than the parties, their legal
                        representatives and any necessary witnesses, such
                        as valuers, may attend.
                    </p>
                    <h2>6. Legal Costs</h2>
                    <p>
                        It is common for each party to be responsible for
                        their own costs if agreement is reached,
                        although <a href="<?= base_url('pay-for-my-divorce')?>"
                        >this is not always the case</a
                        >. For example, if one party has more income
                        and/or capital than the other it may be
                        appropriate that they agree to pay their
                        spouse/civil partner’s legal costs.
                    </p>
                    <p>
                        If the matter proceeds to court, then the judge
                        has the power at any stage of the proceedings,
                        where he or she considers it appropriate, to make
                        an order that one party pays the other party’s
                        costs due to their conduct in relation to any
                        failure to comply with the court rules, failure to
                        disclose financial information, the reasonableness
                        of their position etc.
                    </p>
                    <h2>7. Child Support</h2>
                    <p>
                        The Child Maintenance Service is in charge of the
                        assessment and collection of child maintenance
                        rather than the courts, save in a few exceptional
                        circumstances. The Child Maintenance Service
                        applies a formula in assessing the amount of child
                        support payable. We can assist you in dealing with
                        the Service and run a calculation for you,
                        provided we have the necessary information.
                    </p>
