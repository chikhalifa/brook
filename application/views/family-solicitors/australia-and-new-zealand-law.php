                  <h3>Are prenups recognised in Australia?</h3>
                  <p>
                    Yes. Australia recognises both pre- and post-nuptial
                    agreements (known as ‘financial agreements’) so long as:
                  </p>
                  <ul>
                    <li>
                      The agreement is in writing and signed by both sides
                    </li>
                    <li>
                      Each spouse obtained independent legal advice about the
                      effect of the agreement on his or her rights and the
                      advantages and disadvantages of entering into such an
                      agreement
                    </li>
                    <li>
                      The lawyer giving the advice provides a signed statement
                      of  advice given and that statement is given to the other
                      spouse
                    </li>
                  </ul>
                  <p>
                    In some circumstances, including where there has been fraud,
                    these financial agreements may be set aside. A prenup in
                    England is also likely to be recognised as long as it too
                    has been drawn up in the correct circumstances.
                  </p>
                </div>

                <div class="col-xs-12 col-sm-6">
                  <h3>
                    What kind of factors do the courts in Australia consider
                    when deciding on a financial settlement?
                  </h3>
                  <p>
                    Courts can only make financial orders that are ‘just and
                    equitable’.
                  </p>
                  <p>
                    Under the legislation the courts must consider all direct
                    and indirect financial contributions made by each party. In
                    reaching a decision on finances the courts must also bear in
                    mind the effect the order will have on the earning capacity
                    of either spouse, the duration of the marriage and the
                    extent to which the marriage affected the earning capacity
                    of either party. Courts can also consider the impact any
                    future cohabitation by either spouse may have on finances.
                  </p>
                </div>

                <div class="col-xs-12 col-sm-6">
                  <h3>
                    Do couples have to be separated before seeking a divorce?
                  </h3>
                  <p>
                    Yes. In Australia couples must be separated for at least 12
                    months before issuing a divorce petition. Time spent living
                    under the same roof but separately can count toward the 12
                    month waiting period. This requirement for separation is not
                    necessary in England and Wales.
                  </p>
