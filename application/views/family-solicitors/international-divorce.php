                    <h2 id="heading0">International Divorce: Financial Settlements</h2>
                    <p>
                        Countries around the world have very different approaches to financial
                        settlements on divorce. The UK is known to be flexible when it comes to
                        division of property and spousal maintenance. In contrast some countries
                        work to fixed rules such as a percentage of the main earner’s income. Other
                        countries or jurisdictions have very restrictive rules. These variations in
                        approach by different countries means that getting the jurisdiction right at
                        the beginning is absolutely crucial. It’s also worth remembering that a
                        court order is only effective in the county that made it. There may or may
                        not be ways to enforce the ruling elsewhere and it is important to consider
                        this in advance. If, for example financial assets awarded to one spouse are
                        located elsewhere, that spouse may in some cases have a problem getting what
                        he or she is entitled to. It’s always wise therefore to seek legal advice at
                        the outset so you understand your options.
