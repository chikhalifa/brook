                     <p>
                        In our increasingly complicated world, it is becoming more common that
                        high earning professionals do not just receive salary, but also receive
                        sizeable bonuses, stock options and deferred benefits as part of their
                        remuneration packages.
                    </p>
                    <p>
                        We have long standing expertise in dealing with the challenges that these
                        present, on both sides of the argument.
                    </p>
                    <h2 id="heading0">Base salary</h2>
                    <p>
                        Salary is important in the context of a breakdown of marriage or breakdown
                        of a relationship where there are children, because the base salary will
                        be relevant in respect of either spousal maintenance or child maintenance
                        payable by one to the other. Most base salary elements are fixed and,
                        therefore, it is relatively simple to assess what the per calendar month
                        income will be for that person from this element. In the case of
                        periodical payments for children (child maintenance), there is a set
                        formula of base salary which will be paid towards the care of the child by
                        the absent parent. In respect of periodical payments for a spouse (spousal
                        maintenance), there is no set formula, but the Court will look at the
                        income of the parties, the earning capacity of the party who is seeking
                        periodical payments and the genuine income needs (generously interpreted)
                        of the person who is seeking periodical payments for themselves.
                    </p>
                    <p>
                        Apart from the risk of redundancy, base salary is fairly stable and
                        predictable.
                    </p>
                    <h2 id="heading1">Bonuses</h2>
                    <p>
                        Cash bonuses are paid in addition to one’s base salary. However, there are
                        wide variations and fluctuations as to how cash bonuses are paid. For
                        example, some bonuses are paid on an open “discretionary” basis, some
                        bonuses are paid upon a set of personal and/or company targets being
                        realised and some bonuses are paid on a sliding or uplifting scale,
                        depending on various factors. Most commonly, they are paid annually, but
                        where they are closely linked to sales targets, they can resemble
                        commissions and be paid much more frequently as a short-term incentive.
                    </p>
                    <p>
                        If a cash bonus is being paid, then the question of whether the bonus can
                        be considered a normal part of one’s income and, therefore, added to the
                        family income when considering spousal or child periodical payments. It is
                        important to consider from the viewpoint of the person receiving the bonus
                        that the bonus is not guaranteed and, therefore, it would not make sense
                        from that person’s perspective to have any periodical payments due out of
                        their bonus to be expressed as a figure. It can make more sense for that
                        person to have the amount expressed as a percentage of the bonus. However,
                        then the issue arises as to why, if there is a very large bonus, should
                        perhaps a long-separated spouse receive a windfall much in excess of their
                        stated need?
                    </p>
                    <h2 id="heading2">Bonuses paid as shares or stock options</h2>
                    <p>
                        Traditionally, in order to incentivise staff, companies offer share option
                        schemes. This would normally allow an employee to purchase stock at a
                        certain fixed value, no matter what the actual value at the time of
                        exercise. For example, if 1,000 stock options were granted at a “strike
                        price” of £2.00 per share in 2012, not exercisable for two years and at
                        the time of exercise,&nbsp;the stock market price was £3.00 per share,
                        then the employee would be making a 50% gain. Stock options may not be
                        considered as part of income and they may well add to the capital pot, but
                        it does depend upon the circumstances in which they are granted and, of
                        course, it will be relevant as to whether this is a common occurrence in
                        the income stream of that individual.
                    </p>
                    <p>
                        Deferred vesting is a very common feature, where the options can only be
                        exercised if the employee is still with the same employer after three
                        years for example.
                    </p>
                    <p>
                        Some companies, particularly banks in the past, granted shares to
                        employees as part of their annual remuneration package. Usually, the
                        shares vested into the name of the employee and delivered at the same
                        time. Again, these can usually be considered as an extension of income,
                        rather than adding to the capital pot, but each case will turn on its own
                        facts.
                    </p>
                    <h2 id="heading3">Claw-back</h2>
                    <p>
                        Due to the recession and financial difficulties post 2008, with many
                        financial professionals coming under more scrutiny and condemnation from
                        various circles, a culture of claw-back of bonuses or shares has been
                        instituted in the City. The wider ramifications of this are not yet in
                        play.
                    </p>
                    <p>
                        EC rules now limit bonuses to twice salary, which has led to an explosion
                        of creative ways to define extra payments in other terms than “bonus”.
                    </p>
                    <p>
                        More traditionally, employers especially banks, will seek to claw back
                        certain aspects of their employees’ remuneration packages, for example if
                        they leave the company within a certain period of time a share allotment
                        may be subject to “claw-back”. It is important to note that, in normal
                        circumstances, if a portion of the employee’s remuneration package will be
                        subject to claw-back, then this will very often be offset by any new
                        employer that wants to recruit them, as part of the negotiation package.
                        When acting for the spouse of such an individual, it is important to keep
                        that in mind.
                    </p>
                    <p>
                        Finally, the taxation rules need to be borne in mind when assessing what
                        is to be a fair deal between the spouses.
