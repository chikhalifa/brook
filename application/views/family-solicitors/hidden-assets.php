                    <p>
                        Courts in England and Wales are relatively good at finding and locating
                        hidden assets and are familiar with the fact that sometimes a spouse in a
                        marriage will seek to cover up certain funds or payments which are
                        imminently due. To counter this, the courts encourage what is known as a,
                        “cards on the table” approach. In other words, parties are expected to
                        disclose all their assets fully and frankly.
                    </p>
                    <h3>Using Form E to disclose finances</h3>
                    <p>
                        Parties usually disclose in what is known as a “Form E” which is the
                        financial disclosure form which is required most financial proceedings. Form
                        Es are filled out and exchanged between parties. The purpose of the form is
                        to enable both sides to provide the Court with full details of their
                        finances. Bank statements going back 12 months have to be included as do
                        payslips, business valuations and housing valuations. If the form is
                        complete and there are no further material questions left unanswered, then
                        the chances of settling a case improve.
                    </p>
                    <h3>If either party does not fully disclose</h3>
                    <p>
                        If there is a suspicion that someone has not disclosed all their assets it
                        is possible to ask further questions in the litigation process. The usual
                        document for this is a document simply known as a “Questionnaire.” This
                        allows parties to ask questions about the documents disclosed and query
                        specific transactions. The Questionnaire is particularly useful at, for
                        example, querying unusual bank transfers or entries in bank statements that
                        are suspicious. In most cases the Questionnaire usually effectively locates
                        the majority, if not all of the assets and often locates hidden assets. It
                        is also possible to ask for directions for a forensic accountant to be
                        appointed or to raise further questions or queries in supplementary
                        documents.
                    </p>
                    <h3>Deliberate refusal to disclose</h3>
                    <p>
                        In the case someone is wilfully and deliberately refusing to disclose their
                        assets there are penalties which include cost penalties and even
                        imprisonment. In unusual cases, or if it becomes obvious the assets are
                        being dissipated to deprive a spouse of their entitlement, it may be
                        possible to seek further orders such as an “avoidance of dissipation” order
                        or a worldwide Freezing Order which effectively precludes any substantial
                        dealings prior to litigation and/or retains the assets until the parties
                        agree a settlement or the court makes final orders.
                    </p>
