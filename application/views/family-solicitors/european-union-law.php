                    <h2 id="heading0">Marital Property Across The EU</h2>
                    <p>
                        Individual European states each have their own family law systems with
                        particular approaches to issues like maintenance and child arrangements.
                        Courts in Austria for example might still to some extent take into account
                        the fault of one spouse when considering what level of maintenance to award.
                        And the way individual states approach the overall division of property in a
                        divorce can be markedly different from country to country.
                    </p>
                    <p>
                        Unlike in the UK most EU states allow couples to elect a marital property
                        regime to govern their finances during and after the marriage or impose a
                        particular regime if a couple do not choose one of their own.&nbsp; So
                        couples may be subject to a ‘community property’ regime where property
                        acquired before a marriage is considered separate while property the couple
                        acquire during a marriage (with the exception of inheritances and gifts) is
                        considered shared or ‘community’ property. As an alternative to the
                        community regime a couple may elect to treat all property separately or
                        jointly –whether accrued before or during the marriage.
                    </p>
                    <h2 id="heading1">Enforcing Court Orders Across The EU</h2>
                    <p>
                        One persistent problem that presents itself in international divorce cases
                        is how to enforce a judgment in one country over assets in another. For
                        example if a judge in the UK orders that a holiday home in Spain be
                        transferred to one spouse as part of a financial settlement, how is that
                        transfer finalised in Spain?
                    </p>
                    <p>
                        Enforcement of UK orders in the EU after Brexit is governed by the laws of
                        the relevant EU state. And because not all EU countries have specific rules
                        about recognising non-EU orders (such as an order from an English court) the
                        process can become lengthy and complicated. As a result specialist legal
                        advice on enforcement is advised.
                    </p>
